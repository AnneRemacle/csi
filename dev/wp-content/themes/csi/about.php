	<p>L’ASBL Maison Médicale Centre de Santé Intégré Angleur (CSI Angleur) a vu le jour en 2016. Elle est composée de tous les travailleurs qui le souhaitent et de membres extérieurs.</p> 

	<p>Actuellement, l’équipe pluridisciplinaire de travailleurs se compose de deux médecins généralistes, d’un infirmier et d’une accueillante/employée administrative.</p> 

	<p>Nous fonctionnons en autogestion et au forfait:</p>

	<p>en autogestion: cela signifie qu’il n’y a pas de hiérarchie, la gestion de la maison médicale est assurée par les membres de l’Assemblée générale de l’ASBL.</p>

	<p>au forfait: suite à un accord conclu par notre maison médicale avec l’INAMI et les mutuelles, ces dernières nous versent chaque mois et pour chaque patient inscrit un forfait couvrant les soins médicaux et infirmiers dispensés par l’équipe du CSI Angleur. 
	Ce forfait est fixe, il ne dépend donc pas du nombre de fois que les patients se présentent à la maison médicale. Grâce à ce système, nos patients ne doivent rien débourser quand ils sont soignés par un prestataire de soins de la maison médicale. Cela permet à tout un chacun de bénéficier de soins de qualité. Cela implique également que nos patients s’engagent à rester en ordre de mutuelle (cotisations à l’assurance obligatoire – AO). 
	ATTENTION! Une fois qu’un patient s’est inscrit chez nous, sa mutuelle ne lui remboursera plus les prestations effectuées par un médecin généraliste et/ou un infirmier extérieur au CSI Angleur sauf en cas d’urgence (prévoir un lien vers l’onglet «Vous êtes malade en dehors des heures d’ouverture?»). 
	En revanche, les consultations chez les médecins spécialistes (pédiatre, gynécologue, ORL,…), les soins dentaires, les examens médicaux (radiographie, prise de sang,…) et autres prestations qui ne sont pas du ressort de la maison médicale sont remboursées par la mutuelle comme auparavant.</p>

